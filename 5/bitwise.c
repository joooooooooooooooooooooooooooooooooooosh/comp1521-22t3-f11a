#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>

int main(int argc, char **argv) {
    uint16_t a = 0x4567, b = 0xABCD, c = 0x0001;
    // a     = 0b0100 0101 0110 0111
    // b     = 0b1010 1011 1100 1101
    // a | b = 0b1110 1111 1110 1111
    printf("0x%04x\n", a | b);

    // a     = 0b0100 0101 0110 0111
    // b     = 0b1010 1011 1100 1101
    // a & b = 0b0000 0001 0100 0101
    printf("0x%04x\n", a & b);

    // a     = 0b0100 0101 0110 0111
    // b     = 0b1010 1011 1100 1101
    // a ^ b = 0b1110 1110 1010 1010
    printf("0x%04x\n", a ^ b);

    // a     = 0b0100 0101 0110 0111
    // ~a    = 0b1011 1010 1001 1000
    printf("0x%04x\n", (uint16_t)~a);

    // b     = 0b1010 1011 1100 1101
    // ~b    = 0b0101 0100 0011 0010
    // a     = 0b0100 0101 0110 0111
    // a & ~b= 0b0100 0100 0010 0010
    printf("0x%04x\n", a & ~b);
    
    // c     = 0b0000 0000 0000 0001
    // c << 6= 0b0000 0000 0100 0000
    printf("0x%04x\n", c << 6);

    // a     = 0b0100 0101 0110 0111
    // a >> 4= 0b0000 0100 0101 0110
    printf("0x%04x\n", a >> 4);

    return EXIT_SUCCESS;
}

